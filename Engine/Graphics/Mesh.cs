        public Mesh(float[] vertices)
        {
            _vertices = vertices;
        }

        public IEnumerator<float> GetEnumerator()
        {
            foreach (var vertex in _vertices)
            {
                yield return vertex;
            }
        }

        IEnumerator IEnumerable.GetEnumerator() 
            => GetEnumerator();
        
