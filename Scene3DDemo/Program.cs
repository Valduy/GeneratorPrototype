        public static List<Vector3> FromFloatsToVectors(float[] points)
        {
            var result = new List<Vector3>();

            for (int i = 0; i < points.Length; i += 3)
            {
                result.Add(new Vector3(points[i], points[i + 1], points[i + 2]));
            }

            return result;
        }
